public class city {
    String id;
    String name;
    String p_id;

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setP_id(String p_id) {
        this.p_id = p_id;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getP_id() {
        return p_id;
    }
}
